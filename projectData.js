function renderProjects(projects, containerIndex) {
  const containers = document.querySelectorAll(".ProjectsList");
  const container = containers[containerIndex];
  if (!container) return;

  container.innerHTML = "";

  projects.forEach((project) => {
    const card = document.createElement("span");
    card.classList.add("projectCard");

    card.innerHTML = `
      <a href="${project.demoLink}" target="_blank">
        <img src="${project.imgSrc}" class="projectImg" alt="${project.title}" />
      </a>
      <span class="projectTitle">${project.title}</span>
      <span class="demoViewBtn">
        <a href="${project.demoLink}" class="res-view demoBtn" target="_blank">DEMO</a>
        <a href="${project.codeLink}" class="res-view" id="viewCode" target="_blank">VIEW CODE</a>
      </span>
    `;

    container.appendChild(card);
  });
}
